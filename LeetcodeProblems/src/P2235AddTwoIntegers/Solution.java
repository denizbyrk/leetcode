package P2235AddTwoIntegers;

public class Solution {

    public static int sum(int num1, int num2) {
        
    	return num1 + num2;
    }
    
    public static void main(String[] args) {
    	
    	int num1 = -10;
    	int num2 = 4;
    	
    	int output = Solution.sum(num1, num2);
    	
    	System.out.println(output);
    }
}